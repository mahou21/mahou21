/*
Skriv et program der udregner og udskriver omkredsen (2·π·r) af tre cirklermed radius på hhv. 1, 3 og 5,
og afslutter med at udskriven summen af disse.
 */
public class Main {
    public static void main(String[] args) {
        int c1 = 1;
        int c2 = 3;
        int c3 = 5;

        System.out.println("Circle 1 = "+ 2*3.14*c1);
        System.out.println("Circle 2 = "+ 2*3.14*c2);
        System.out.println("Circle 3 = "+ 2*3.14*c3);
    }
}
